
public class ProcessaCondicao {

	public static void main(String[] args) {
		// Criando uma condição de análise com if
		
		//Criei uma variável
		int time = 15;
		//Análise condicional
		if(time < 18 ) {
			System.out.println("Bom Tarde!!");
		} else {
			System.out.println("Boa Noite!!");
		}

	}

}
