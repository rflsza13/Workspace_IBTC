
public class MostrarMensagem {

	public static void main(String[] args) {
		// Criando uma mensagem para aparecer na tela
		System.out.println("Seja Bem-Vindo Rafael Souza !!");
		System.out.println("Super desenvolvimento em Java!!");
		
		// Criando variaveis em Java
		String nome = "Rafael Rodrigues de Souza";
		int idade = 28;
		System.out.println("Olá " + nome + ", a sua idade é " + idade);
		
		// Efetuando Cálculos no Java
		int num1 = 100, num2 = 200;
		int rest = num1 + num2;
		System.out.println("O valor é: " + rest);
	}

}
